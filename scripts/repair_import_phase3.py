"""
Import Phase 3 data: equipment + stock movements + asset handovers.

Source: same xlsx as Phase 1 (Repair/ใบแจ้งซ่อม.xlsx).
Sheets used:
  - อุปกรณ์ (998 rows)             → rpr_equipment
  - StockDetail (931 rows)        → rpr_stock_moves
  - ใบส่งมอบทรัพย์สิน (987 rows)  → rpr_handovers + rpr_handover_items
    (flat sheet: each row = one item, shared doc_no per handover)

Run: python scripts/repair_import_phase3.py
Then run the generated SQL in Supabase SQL Editor.
"""
import io, sys
from pathlib import Path
import openpyxl

if sys.stdout.encoding != 'utf-8':
    sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')

XLSX = Path(__file__).parent.parent / 'Repair' / 'ใบแจ้งซ่อม.xlsx'
OUT  = Path(__file__).parent / 'repair_import_phase3.sql'

def esc(s):
    if s is None: return 'null'
    s = str(s).strip()
    if not s or s.lower() in ('none', 'null'): return 'null'
    return "'" + s.replace("'", "''") + "'"

def num(v):
    if v is None: return 'null'
    try:
        return str(float(str(v).strip()))
    except: return 'null'

def ts(v):
    if v is None: return 'null'
    s = str(v).strip()
    if not s: return 'null'
    return "'" + s.replace("'", "''") + "'::timestamptz"

def ts_default(v):
    return ts(v) if v else 'default'

wb = openpyxl.load_workbook(XLSX, data_only=True)
lines = [
    "-- Auto-generated by scripts/repair_import_phase3.py",
    "-- Idempotent: uses ON CONFLICT DO NOTHING.",
    "",
    "begin;",
    "",
]

# ───────────────────────────────────────────────────────────
# 1. rpr_equipment from อุปกรณ์ sheet
# ───────────────────────────────────────────────────────────
ws = wb['อุปกรณ์']
EQ = {'category': 0, 'stock_id': 1, 'name': 2, 'in': 4, 'on_hand': 5, 'image': 7}
eq_count = 0
lines.append("-- ===== rpr_equipment =====")
for r in range(2, ws.max_row + 1):
    def c(k): return ws.cell(r, EQ[k] + 1).value
    sid = c('stock_id')
    if not sid or not str(sid).strip(): continue
    sid = str(sid).strip()
    if not c('category') and not c('name'): continue
    lines.append(
        f"insert into rpr_equipment (stock_id, category, name, quantity_in, "
        f"quantity_on_hand, image_url) values ("
        f"{esc(sid)}, {esc(c('category'))}, {esc(c('name'))}, "
        f"{num(c('in'))}, {num(c('on_hand'))}, {esc(c('image'))}) "
        f"on conflict (stock_id) do nothing;"
    )
    eq_count += 1
lines.append("")

# ───────────────────────────────────────────────────────────
# 2. rpr_stock_moves from StockDetail sheet
# ───────────────────────────────────────────────────────────
ws = wb['StockDetail']
SM = {
    'performed_by': 0, 'stock_id': 1, 'movement_type': 2,
    'stock_name': 4, 'quantity': 7, 'floor': 9, 'zone': 10,
    'performed_at': 13, 'job_id': 14,
}
sm_count = 0
lines.append("-- ===== rpr_stock_moves =====")
for r in range(2, ws.max_row + 1):
    def c(k): return ws.cell(r, SM[k] + 1).value
    if not c('stock_id') and not c('performed_by'): continue
    # stock_id often has " : UNIQUEID" suffix — strip it
    sid = c('stock_id')
    if sid:
        sid = str(sid).split(':')[0].strip()
    # FK to rpr_equipment — only insert if stock_id is present + valid format
    if not sid: continue
    lines.append(
        f"insert into rpr_stock_moves (stock_id, stock_name, movement_type, "
        f"quantity, performed_by, floor, zone, job_id, performed_at) values ("
        f"{esc(sid)}, {esc(c('stock_name'))}, {esc(c('movement_type'))}, "
        f"{num(c('quantity'))}, {esc(c('performed_by'))}, "
        f"{esc(c('floor'))}, {esc(c('zone'))}, {esc(c('job_id'))}, "
        f"{ts_default(c('performed_at'))});"
    )
    sm_count += 1
lines.append("")

# ───────────────────────────────────────────────────────────
# 3. rpr_handovers + items from ใบส่งมอบทรัพย์สิน (denormalized)
# Group by เลขที่เอกสาร — first row sets the doc header, every row
# inserts as an item.
# ───────────────────────────────────────────────────────────
ws = wb['ใบส่งมอบทรัพย์สิน']
HO = {
    'created_at': 0, 'delivered_at': 1, 'addr': 2, 'addr_other': 3,
    'stock_id': 6, 'item': 7, 'quantity': 8, 'unit': 9, 'note': 10,
    'recipient': 11, 'sender': 12, 'doc_no': 13, 'authorized': 14,
}
docs_seen = set()
ho_doc_count = 0
ho_item_count = 0
synth_seq = 0
lines.append("-- ===== rpr_handovers + rpr_handover_items =====")
for r in range(2, ws.max_row + 1):
    def c(k): return ws.cell(r, HO[k] + 1).value
    doc_no = c('doc_no')
    sender = c('sender')
    if not doc_no and not sender and not c('item'):
        continue
    if not doc_no or not str(doc_no).strip():
        synth_seq += 1
        doc_no = f'HO-AUTO-{synth_seq:04d}'
    doc_no = str(doc_no).strip()

    # Insert parent only once per doc_no
    if doc_no not in docs_seen:
        docs_seen.add(doc_no)
        lines.append(
            f"insert into rpr_handovers (doc_no, created_at, delivered_at, "
            f"recipient_addr, recipient_addr_other, sender, recipient, "
            f"authorized_person) values ("
            f"{esc(doc_no)}, {ts_default(c('created_at'))}, "
            f"{ts(c('delivered_at'))}, {esc(c('addr'))}, {esc(c('addr_other'))}, "
            f"{esc(c('sender'))}, {esc(c('recipient'))}, {esc(c('authorized'))}) "
            f"on conflict (doc_no) do nothing;"
        )
        ho_doc_count += 1

    # Item line — only if there's actually an item to insert
    if c('item') or c('quantity'):
        lines.append(
            f"insert into rpr_handover_items (doc_no, item, quantity, unit, note) "
            f"values ({esc(doc_no)}, {esc(c('item'))}, {num(c('quantity'))}, "
            f"{esc(c('unit'))}, {esc(c('note'))});"
        )
        ho_item_count += 1

lines.append("")
lines.append("commit;")
lines.append("")
lines.append(f"-- Counts:")
lines.append(f"--   rpr_equipment       : {eq_count}")
lines.append(f"--   rpr_stock_moves     : {sm_count}")
lines.append(f"--   rpr_handovers       : {ho_doc_count}")
lines.append(f"--   rpr_handover_items  : {ho_item_count}")

OUT.write_text('\n'.join(lines), encoding='utf-8')
print(f'Wrote {OUT}')
print(f'  Equipment:       {eq_count}')
print(f'  Stock moves:     {sm_count}')
print(f'  Handovers (doc): {ho_doc_count}')
print(f'  Handover items:  {ho_item_count}')
